# Отобразите три наиболее распространенных слова в man для команды bash длиной не менее 4 символов.

# Вызываем команду `man bash`, чтобы получить руководство по bash
man bash |

# Используем `tr` для замены всех символов, которые не являются буквенно-цифровыми, на символ новой строки
# -c: заменяет все символы, которые не соответствуют `[:alnum:]` (буквы и цифры)
# -s: сжимает повторяющиеся символы новой строки
# '[\n*]': указывает, что заменяем на символ новой строки
tr -cs '[:alnum:]' '[\n*]' |

# Используем `awk`, чтобы фильтровать слова длиной 4 символа и более
# `length($0) >= 4`: выбирает строки, где длина слова больше или равна 4 символам
awk 'length($0) >= 4' |

# Сортируем результат в алфавитном порядке
sort |

# Убираем повторяющиеся строки и подсчитываем их количество
uniq -c |

# Сортируем по количеству вхождений в порядке убывания
sort -nr |

# Выводим первые 3 самых частых слова
head -3
