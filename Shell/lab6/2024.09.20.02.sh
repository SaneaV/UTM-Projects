# Задайте еще один однократный запуск скрипта из пункта 1 через 2 минуты. Организуйте слежение за файлом ~/report и выведите на консоль новые строки из этого файла, как только они появятся.

#!/bin/bash

# Планирование однократного запуска вашего скрипта через 2 минуты
# Команда echo передаёт скрипт в планировщик задач at, который выполнит его через 2 минуты.
echo "/path/to/your_script.sh" | at now + 2 minutes

# Ожидание появления новых строк в файле ~/report и вывод их на консоль
# tail -f следит за файлом ~/report и выводит на консоль новые строки по мере их добавления.
echo "Watching ~/report for new lines..."
tail -f ~/report
