# Определение рекурсивной функции для вычисления суммы первых N натуральных чисел
def sum_of_natural_numbers(n):
    if n == 1:  # Базовый случай: если n равно 1, вернуть 1
        return 1
    else:
        return n + sum_of_natural_numbers(n - 1)  # Рекурсивный случай: сумма текущего числа и суммы первых (n-1) чисел

# Пример вызова функции с числом N
N = 5
result = sum_of_natural_numbers(N)
print(f"Сумма первых {N} натуральных чисел:", result)