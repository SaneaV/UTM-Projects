def вычислить_среднее(оценка1=4, оценка2=4, оценка3=4):
    # Вычисляем среднее значение оценок
    среднее = (оценка1 + оценка2 + оценка3) / 3
    return среднее

# Вызываем функцию с разными комбинациями позиционных и именованных аргументов
результат1 = вычислить_среднее(8, 7, 9)  # Оценка указана для всех трех аргументов
результат2 = вычислить_среднее(6, 5)     # Оценка указана только для первых двух аргументов, третья оценка по умолчанию 4
результат3 = вычислить_среднее()          # Все оценки по умолчанию равны 4

# Выводим результаты
print(f"Среднее для всех трех оценок: {результат1}")
print(f"Среднее для первых двух оценок: {результат2}")
print(f"Среднее для всех оценок по умолчанию: {результат3}")
