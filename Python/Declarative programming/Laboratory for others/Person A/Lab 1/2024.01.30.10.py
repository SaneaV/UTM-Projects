def сумма_первых_n_натуральных(n):
    # Базовый случай: сумма первых 0 натуральных чисел равна 0
    if n == 0:
        return 0
    # Рекурсивный случай: добавляем текущее число и вызываем функцию для предыдущего числа
    else:
        return n + сумма_первых_n_натуральных(n - 1)

# Пример использования функции
N = 5
результат = сумма_первых_n_натуральных(N)

# Выводим результат
print(f"Сумма первых {N} натуральных чисел: {результат}")
