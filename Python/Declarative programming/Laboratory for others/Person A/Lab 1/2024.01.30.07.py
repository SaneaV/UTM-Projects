def конкатенировать_строки(*args):
    # Используем функцию join() для конкатенации всех строк, переданных в аргументах
    результат = ''.join(args)
    return результат

# Пример использования функции
результирующая_строка = конкатенировать_строки("Первая", " вторая", " третья", " и так далее.")
print("Результат конкатенации:", результирующая_строка)
