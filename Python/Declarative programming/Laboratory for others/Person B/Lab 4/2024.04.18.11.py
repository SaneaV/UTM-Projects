# Вычисление 1/2 + 1/3 в арифметике рациональных чисел.

# Импорт класса Fraction из модуля fractions
from fractions import Fraction

# Вычисление суммы 1/2 + 1/3
result = Fraction(1, 2) + Fraction(1, 3)

# Вывод результата
print("Результат сложения 1/2 + 1/3 в арифметике рациональных чисел:", result)