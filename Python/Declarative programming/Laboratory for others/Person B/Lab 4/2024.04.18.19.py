# –ò–º–ø–æ—Ä—Ç —Ñ—É–Ω–∫—Ü–∏–∏ quad() –∏–∑ –±–∏–±–ª–∏–æ—Ç–µ–∫–∏ scipy
from scipy.integrate import quad

from math import cos, pi

# –û–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏, –∫–æ—Ç–æ—Ä–∞—è –±—É–¥–µ—Ç –ø—Ä–æ–∏–Ω—Ç–µ–≥—Ä–∏—Ä–æ–≤–∞–Ω–∞
def integrand(x):
    return cos(2 * pi * x)

# –í—ã–∑–æ–≤ —Ñ—É–Ω–∫—Ü–∏–∏ quad() –¥–ª—è –≤—ã—á–∏—Å–ª–µ–Ω–∏—è –∏–Ω—Ç–µ–≥—Ä–∞–ª–∞
integral, error = quad(integrand, 0, 1)

# –í—ã–≤–æ–¥ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞
print("–ó–Ω–∞—á–µ–Ω–∏–µ –∏–Ω—Ç–µ–≥—Ä–∞–ª–∞:", integral)
