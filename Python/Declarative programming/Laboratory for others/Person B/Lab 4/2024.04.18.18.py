# –†–µ—à–∏—Ç–µ –¥–∏—Ñ—Ñ–µ—Ä–µ–Ω—Ü–∏–∞–ª—å–Ω–æ–µ —É—Ä–∞–≤–Ω–µ–Ω–∏–µ –ë–µ—Ä–Ω—É–ª–ª–∏ ùë• * ùëëùëì(ùë•) / ùëëùë• + ùëì(ùë•) ‚àí ùëì(ùë•)^2 = 0. –†–µ—à–∏—Ç–µ
# —Ç–æ –∂–µ —É—Ä–∞–≤–Ω–µ–Ω–∏–µ, –∏—Å–ø–æ–ª—å–∑—É—è hint='Bernoulli'. –ß—Ç–æ –≤—ã –∑–∞–º–µ—á–∞–µ—Ç–µ?

# –ò–º–ø–æ—Ä—Ç –º–æ–¥—É–ª—è sympy
import sympy as sp

# –û–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ —Å–∏–º–≤–æ–ª–æ–≤ x –∏ f(x)
x = sp.symbols('x')
f = sp.Function('f')(x)

# –û–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ —É—Ä–∞–≤–Ω–µ–Ω–∏—è –¥–∏—Ñ—Ñ–µ—Ä–µ–Ω—Ü–∏–∞–ª—å–Ω–æ–≥–æ —É—Ä–∞–≤–Ω–µ–Ω–∏—è –ë–µ—Ä–Ω—É–ª–ª–∏
eq = x * sp.diff(f, x) + f - f**2

# –†–µ—à–µ–Ω–∏–µ —É—Ä–∞–≤–Ω–µ–Ω–∏—è –±–µ–∑ –ø–æ–¥—Å–∫–∞–∑–∫–∏
solution_no_hint = sp.dsolve(eq, f)

# –†–µ—à–µ–Ω–∏–µ —É—Ä–∞–≤–Ω–µ–Ω–∏—è —Å –ø–æ–¥—Å–∫–∞–∑–∫–æ–π 'Bernoulli'
solution_with_hint = sp.dsolve(eq, f, hint='Bernoulli')

# –í—ã–≤–æ–¥ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–≤
print("–†–µ—à–µ–Ω–∏–µ –±–µ–∑ –ø–æ–¥—Å–∫–∞–∑–∫–∏:", solution_no_hint)
print("–†–µ—à–µ–Ω–∏–µ —Å –ø–æ–¥—Å–∫–∞–∑–∫–æ–π 'Bernoulli':", solution_with_hint)

