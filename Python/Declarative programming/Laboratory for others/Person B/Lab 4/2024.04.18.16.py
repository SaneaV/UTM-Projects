# –†–µ—à–µ–Ω–∏–µ —Å–∏—Å—Ç–µ–º—ã —É—Ä–∞–≤–Ω–µ–Ω–∏–π 2ùë• + 3ùë¶ = 5, 4ùë• ‚àí 3ùë¶ = ‚àí4.

# –ò–º–ø–æ—Ä—Ç –º–æ–¥—É–ª—è sympy
import sympy as sp

# –û–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ —Å–∏–º–≤–æ–ª–æ–≤ x –∏ y
x, y = sp.symbols('x y')

# –û–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ —Å–∏—Å—Ç–µ–º—ã —É—Ä–∞–≤–Ω–µ–Ω–∏–π
eq1 = sp.Eq(2*x + 3*y, 5)
eq2 = sp.Eq(4*x - 3*y, -4)

# –†–µ—à–µ–Ω–∏–µ —Å–∏—Å—Ç–µ–º—ã —É—Ä–∞–≤–Ω–µ–Ω–∏–π
solution = sp.solve((eq1, eq2), (x, y))

# –í—ã–≤–æ–¥ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞
print("–†–µ—à–µ–Ω–∏–µ —Å–∏—Å—Ç–µ–º—ã —É—Ä–∞–≤–Ω–µ–Ω–∏–π:", solution)