# Определение рекурсивной функции, которая выводит сумму первых N натуральных чисел.

def sum_of_natural_numbers(n):
    # Базовый случай: если n равно 0, вернуть 0
    if n == 0:
        return 0
    # Рекурсивный случай: вернуть n плюс сумму первых (n-1) натуральных чисел
    else:
        return n + sum_of_natural_numbers(n - 1)

# Вывод суммы первых 10 натуральных чисел
print("Сумма первых 10 натуральных чисел:", sum_of_natural_numbers(10))
