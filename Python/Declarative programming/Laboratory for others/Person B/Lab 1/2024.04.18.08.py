# Определение функции, которая вычисляет среднее значение трех указанных оценок. 
# Если не все оценки указаны, будут использованы значения по умолчанию, равные 4. 
# Вызов функции с различными комбинациями позиционных и ключевых аргументов.

# Определение функции для вычисления среднего значения оценок
def calculate_average(grade1=4, grade2=4, grade3=4):
    total_grades = grade1 + grade2 + grade3
    average = total_grades / 3
    return average

# Вызов функции с различными комбинациями аргументов
# Вызов функции с тремя позиционными аргументами
print("Среднее значение с тремя указанными оценками:", calculate_average(6, 7, 8))
# Вызов функции с двумя позиционными аргументами
print("Среднее значение с двумя указанными оценками:", calculate_average(6, 7))
# Вызов функции с одним позиционным аргументом
print("Среднее значение с одной указанной оценкой:", calculate_average(6))
# Вызов функции с использованием ключевых аргументов
print("Среднее значение с использованием ключевых аргументов:", calculate_average(grade1=6, grade3=8))
# Вызов функции без указания аргументов (использование значений по умолчанию)
print("Среднее значение с использованием значений по умолчанию:", calculate_average())
