# Создание словаря с использованием строк в качестве ключей и чисел с плавающей точкой в качестве значений.
# Печать только ключей словаря, а затем кортежей, составленных из ключей и значений.

# Создание пустого словаря
dictionary = {}

# Добавление элементов в словарь
dictionary['яблоко'] = 2.5
dictionary['апельсин'] = 3.0
dictionary['банан'] = 1.8

# Печать только ключей словаря
print("Ключи словаря:")
for key in dictionary.keys():
    print(key)

# Печать кортежей, составленных из ключей и значений
print("\nКортежи из ключей и значений:")
for key, value in dictionary.items():
    print((key, value))