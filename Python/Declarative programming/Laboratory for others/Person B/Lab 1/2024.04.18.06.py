# Определение функции, которая объединяет любое количество строк, заданных на входе.
def concatenate_strings(*args):
    # Использование метода join() для объединения строк в одну
    concatenated_string = ''.join(args)
    return concatenated_string

# Пример использования функции
# Вызов функции с несколькими строками в качестве аргументов
result = concatenate_strings("Hello", " ", "world", "!")
# Вывод результата
print("Результат объединения строк:", result)