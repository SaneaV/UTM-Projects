% Создаем вектор времени от 0 до 1.7 с шагом 0.001
t = 0:0.001:1.7;

% Задаем параметры для генерации сигнала
T = 0.006;
k = 101:1701;

% Создаем вектор нулей размером 100
Z = zeros(1, 100);

% Генерируем сигнал, который представляет собой экспоненциально затухающую гармоническую волну
x = [Z, exp(-(k * T - 0.1) / 0.2) .* sin(2 * pi * (k * T - 0.1) / 0.16)];

% Строим график сигнала с сеткой
plot(t, x);
grid on;

% Добавляем заголовок графика и подписи к осям для ясности
title('Процесс транзиторий');
xlabel('Время (с)');
ylabel('Амплитуда');

