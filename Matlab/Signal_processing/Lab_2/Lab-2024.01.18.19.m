% Создаем вектор значений k от -10 до 0
k = -10:0;

% Инициализируем вектор x3 нулями
x3 = zeros(size(k));

% Устанавливаем значение x3(2) равным 1, чтобы создать единичный импульс
x3(2) = 1;

% Создаем новую фигуру и строим график дискретной последовательности
figure;
stem(k, x3);
grid on;

% Добавляем подписи к осям для ясности
xlabel('k');
ylabel('x3(k)');
title('Дискретный импульс при k=2');

