% Создаем вектор значений k от -10 до 10
k = -10:10;

% Инициализируем вектор x2 нулями
x2 = zeros(size(k));

% Устанавливаем значение x2(14) равным 0.1, чтобы создать импульс с амплитудой 0.1
x2(14) = 0.1;

% Создаем новую фигуру и строим график дискретного импульса
figure;
stem(k, x2);
grid on;

% Добавляем подписи к осям для ясности
xlabel('k');
ylabel('x2(k)');
title('Дискретный импульс с амплитудой 0.1');

