% Создание вектора времени от 0 до 50 с шагом 0.01
t = 0:0.01:50;

% Генерация сигнала yi(t) как функции sinc с параметрами
% Амплитуда: 0.3
% Период: 6 (из-за pi в знаменателе)
% Смещение по времени: 17
yi = 0.3 * sinc(pi * (t - 17) / 3);

% Построение графика сигнала
plot(t, yi);
grid

% Добавление подписи по оси x
xlabel('timpul (s)');

% Добавление подписи по оси y
ylabel('yi(t)');
