% Создаем вектор времени от 0 до 1 с шагом 0.009
t = 0:0.009:1;

% Генерируем сигнал, представляющий собой сумму двух синусоид с различными частотами
y = sin(3 * pi * 50 * t) + 2 * sin(2 * pi * 120 * t);

% Задаем начальное состояние генератора случайных чисел
randn('state', 0);

% Генерируем случайный шум и добавляем его к сигналу
yn = y + 0.5 * randn(size(t));

% Строим график первых 50 отсчетов сигнала с шумом
plot(t(1:50), yn(1:50));
grid on;

% Добавляем подписи к осям для ясности
xlabel('Время (с)');
ylabel('Амплитуда');

