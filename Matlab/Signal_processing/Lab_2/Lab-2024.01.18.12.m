% Задаем значения параметров
B = 100;    % Амплитуда сигнала
a = 4;      % Параметр, влияющий на скорость убывания сигнала

% Создаем вектор времени от 0 до 1 с шагом 0.001
t = 0:0.001:1;

% Генерируем экспоненциально убывающий сигнал
x = B * exp(-a * t);

% Строим график сигнала с сеткой
plot(t, x);
grid on;

% Добавляем подписи к осям для ясности
xlabel('Время (с)');
ylabel('Амплитуда');
title('Экспоненциально убывающий сигнал');

