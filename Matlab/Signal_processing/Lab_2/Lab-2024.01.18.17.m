% Создаем вектор значений k от 1 до 20
k = 1:20;

% Инициализируем вектор x1 нулями
x1 = zeros(size(k));

% Устанавливаем значение x1(9) равным 1, чтобы создать единичный импульс
x1(9) = 1;

% Создаем новую фигуру и строим график дискретного единичного импульса
figure;
stem(k, x1);
grid on;

% Добавляем подписи к осям для ясности
xlabel('k');
ylabel('x1(k)');
title('Дискретный единичный импульс');

