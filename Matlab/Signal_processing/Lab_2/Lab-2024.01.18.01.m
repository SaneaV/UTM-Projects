```matlab
% Определение временной оси от 0 до 50 с шагом 0.01
t = 0:0.01:50;

% Генерация сигнала, вычисленного с использованием sinc-функции
yi = 0.9 * sinc(pi * (t - 25) / 5);

% Построение графика сигнала
plot(t, yi);
grid;

% Подписи осей
xlabel('Время (с)');
ylabel('yi(t)');
```
