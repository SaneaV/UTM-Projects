% Создаем вектор значений k от 0 до 300
k = 0:300;

% Генерируем синусоиду с частотой 1/20 и амплитудой 1
x = sin(2 * pi * k / 20);

% Строим график синусоиды с сеткой
plot(x);
grid on;

% Добавляем заголовок графика
title('Синусоида');

% Добавляем подписи к осям для ясности
xlabel('Номер отсчета');
ylabel('Амплитуда');

