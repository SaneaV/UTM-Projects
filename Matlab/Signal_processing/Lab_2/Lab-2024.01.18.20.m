% Создаем вектор времени t от -1 до 1 с шагом 0.002
t = -1:0.002:1;

% Создаем вектор U1, представляющий собой скачок от 0 до 1 в первой половине времени
U1 = [zeros(1, 250), ones(1, 751)];

% Создаем вектор U2, представляющий собой скачок от 0 до 1 во второй половине времени
U2 = [zeros(1, 751), ones(1, 250)];

% Формируем сигнал U, вычитая второй скачок из первого
U = U1 - U2;

% Строим график сигнала U в зависимости от времени t
plot(t, U);
grid on;

% Добавляем подписи к осям для ясности
xlabel('t');
ylabel('U(t)');
title('Сигнал с скачком в момент времени t=0');

