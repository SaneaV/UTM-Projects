% Задаем значения параметров
B = 1;     % Амплитуда сигнала
r = 0.9;   % Фактор убывания экспоненты

% Создаем вектор времени n от -10 до 10
n = -10:10;

% Генерируем дискретный экспоненциальный сигнал
x = B * r.^n;

% Строим график сигнала в виде дискретных точек
stem(n, x);

% Добавляем подписи к осям для ясности
xlabel('n');
ylabel('Амплитуда');
title('Дискретный экспоненциальный сигнал');

