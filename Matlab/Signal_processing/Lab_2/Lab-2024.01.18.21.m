% Загружаем пакет signal
pkg load signal;

% Задаем параметры сигнала
A = 7;             % Амплитуда
omega = pi / 3;    % Частота в радианах
rho = 0.5;         % Коэффициент заполнения

% Создаем вектор времени n от -30 до 30
n = -30:30;

% Генерируем дискретную квадратную волну
x = A * square(omega * n * rho);

% Строим график дискретной последовательности
stem(n, x);
grid on;

% Добавляем подписи к осям для ясности
xlabel('n');
ylabel('x(n)');
title('Дискретная квадратная волна');

