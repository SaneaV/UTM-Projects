% Генерация ансамбля сигналов с случайным шумом
R = 100; % Количество реализаций
T = 100; % Количество точек данных в каждой реализации
ensemble = sin(0.1 * (1:T)) + 0.2 * randn(R, T);

% Расчет ансамблевого среднего
ensemble_average = mean(ensemble, 1);

% Построение графика: индивидуальные реализации и ансамблевое среднее
figure;
plot(1:T, ensemble', 'Color', [0.7, 0.7, 0.7]); % Серые линии для индивидуальных реализаций
hold on;
plot(1:T, ensemble_average, 'k-', 'LineWidth', 2); % Черная линия для ансамблевого среднего
xlabel('Время');
ylabel('Амплитуда');
title('Ансамблевое Среднее');
legend('Индивидуальные Реализации', 'Ансамблевое Среднее');

