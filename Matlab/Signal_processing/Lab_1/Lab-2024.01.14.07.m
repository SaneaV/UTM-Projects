% Среднее арифметическое / Сглаживание сигнала с использованием скользящего среднего

% Создание временной оси
t = linspace(0, 1, 100);

% Генерация сигнала с добавлением шума
signal = sin(2*pi*5*t) + 0.5*cos(2*pi*10*t) + 0.2*randn(size(t));

% Определение размера окна для среднего
window_size = 5;

% Применение оконного среднего
smoothed_signal = movmean(signal, window_size);

% Отображение результатов
figure;
plot(t, signal, 'b-', 'LineWidth', 1.5);
hold on;
plot(t, smoothed_signal, 'r-', 'LineWidth', 1.5);
legend('Исходный сигнал', 'Сглаженный сигнал');
xlabel('Время');
ylabel('Амплитуда');
title('Сглаживание сигнала с использованием скользящего среднего');

