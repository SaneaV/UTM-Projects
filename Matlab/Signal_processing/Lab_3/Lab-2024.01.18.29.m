% Скрипт FCOSINUS вычисляет преобразование Фурье
% для симметричного относительно начала косинусоидального импульса
% с параметрами: длительность a и общая площадь под кривой 1

a = 7;

syms x w % объявление символьных переменных
integral_result = int(pi/(2*a) * cos(pi*x/a) * exp(-j*w*x), -a/2, a/2);
% вычисление интеграла Фурье

% Построение графика
figure;
ezplot(integral_result, [-5 5]);
hold on;

u = -5:1:5;
y = zeros(size(u));
plot(u, y); % рисование горизонтальной линии y=0
grid on;
hold off;

title('Преобразование Фурье для косинусоидального импульса');
xlabel('w');
ylabel('Amplitudine');

